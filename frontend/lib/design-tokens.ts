/**
 * Get a color value by path
 * @param path - Dot notation path to the color (e.g., 'primary.500')
 * @returns The color value
 */
export function getColor(path: string): string {
  const keys = path.split('.');
  let value: any = colors;
  
  for (const key of keys) {
    value = value[key];
    if (value === undefined) {
      throw new Error(`Color path "${path}" not found`);
    }
  }
  
  return value;
}

/**
 * Get spacing value
 * @param size - Spacing size key
 * @returns The spacing value
 */
export function getSpacing(size: keyof typeof spacing): string {
  return spacing[size];
}

/**
 * Get typography value
 * @param category - Typography category (fontSize, fontWeight, etc.)
 * @param size - Size key within the category
 * @returns The typography value
 */
export function getTypography(category: keyof typeof typography, size: string): string {
  const categoryValues = typography[category] as Record<string, string>;
  return categoryValues[size] || '';
}

